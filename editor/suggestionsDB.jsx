function extractKeywords(text) {
  if (!text) return [];
  return text.toLowerCase().split(/\W+/).filter(w => w.length > 2);
}

export function generateSuggestions(resumeData, domain) {
  // Get base domain suggestions
  const domainTips = suggestionsDB[domain] || suggestionsDB['IT'];

  // Extract text from resume
  const text = [
    resumeData.role || '',
    resumeData.summary || '',
    (resumeData.skills || []).join(' '),
    (resumeData.experience || []).map(e => e.description || '').join(' ')
  ].join(' ');

  const words = extractKeywords(text);
  
  // Only add contextual tips that match the current domain
  let contextual = [];
  words.forEach(word => {
    if (keywordMap[word] && keywordMap[word].domain === domain) {
      contextual.push(...keywordMap[word].tips);
    }
  });

  // Merge and deduplicate
  const all = [...new Set([...contextual, ...domainTips])];
  
  return all.slice(0, 50);
}